using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GraphPack
{
    public abstract class GraphFactory
    {
        
        //****************************************************************************************************************************************************************
        //Construction and initialization
        //****************************************************************************************************************************************************************        
        public GraphFactory(EdgeMetricFactory EMF)
        {
            this.EMF = EMF;
        }


        //****************************************************************************************************************************************************************
        //Factory methods
        //****************************************************************************************************************************************************************        
        public abstract Graph CreateGraph();
        public abstract Graph CreateGraph(string ID);
        public abstract EdgeMetric CreateEdgeMetric(double[] Metrics);


        //****************************************************************************************************************************************************************
        //Locals
        //****************************************************************************************************************************************************************
        private EdgeMetricFactory EMF;

    }
}
